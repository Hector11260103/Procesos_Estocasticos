import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from statsmodels.tsa.stattools import adfuller
from statsmodels.tsa.arima.model import ARIMA
from pmdarima import auto_arima  # Para encontrar los mejores parÃ¡metros automÃ¡ticamente

# ğŸ“Œ 1. Cargar el Dataset
file_path = "dataset_transformado.csv"  # Ajusta el nombre del archivo
df = pd.read_csv(file_path)

# ğŸ“Œ 2. Verificar si la columna 'Fecha' existe y convertirla a formato datetime
if 'Fecha' not in df.columns:
    raise ValueError("La columna 'Fecha' no estÃ¡ en el dataset. Verifica el archivo CSV.")

df['Fecha'] = pd.to_datetime(df['Fecha'], errors='coerce')  # Convertir a datetime
df = df.dropna(subset=['Fecha'])  # Eliminar filas con fechas invÃ¡lidas
df.set_index('Fecha', inplace=True)  # Usar Fecha como Ã­ndice

# ğŸ“Œ 3. Convertir 'Valor' a numÃ©rico y eliminar valores no numÃ©ricos
df['Valor'] = pd.to_numeric(df['Valor'], errors='coerce')
df = df.dropna(subset=['Valor'])  # Eliminar filas con valores nulos en 'Valor'

# ğŸ“Œ 4. Visualizar la Serie Temporal Original
plt.figure(figsize=(10, 5))
plt.plot(df.index, df['Valor'], label="Serie Original", color="blue")
plt.title("ğŸ“ˆ Serie Temporal Original")
plt.xlabel("Fecha")
plt.ylabel("Valor")
plt.legend()
plt.grid()
plt.xticks(rotation=45)
plt.show()

# ğŸ“Œ 5. Prueba de Estacionariedad (ADF)
print("\nğŸ” Prueba de Estacionariedad ADF:")
adf_test = adfuller(df['Valor'])
print(f"   ğŸ“Š EstadÃ­stica ADF: {adf_test[0]}")
print(f"   ğŸ“Š P-valor: {adf_test[1]}")
print(f"   ğŸ“Š Valores CrÃ­ticos: {adf_test[4]}")
if adf_test[1] > 0.05:
    print("   âŒ La serie NO es estacionaria. Aplicando diferenciaciÃ³n...")
    df['Valor_Diff'] = df['Valor'].diff().dropna()
    adf_test_diff = adfuller(df['Valor_Diff'].dropna())
    print(f"   ğŸ“Š P-valor despuÃ©s de diferenciaciÃ³n: {adf_test_diff[1]}")
else:
    print("   âœ… La serie es estacionaria.")

# ğŸ“Œ 6. Determinar los Mejores ParÃ¡metros ARIMA (p,d,q) AutomÃ¡ticamente
print("\nğŸ” Buscando los mejores parÃ¡metros ARIMA...")
modelo_auto = auto_arima(df['Valor'].dropna(), seasonal=False, stepwise=True, trace=True)
p, d, q = modelo_auto.order  # Extraer los mejores valores de p, d, q
print(f"   ğŸ“Œ ParÃ¡metros Ã³ptimos encontrados: ARIMA({p},{d},{q})")

# ğŸ“Œ 7. Ajustar Modelo ARIMA con los ParÃ¡metros Ã“ptimos
modelo_arima = ARIMA(df['Valor'], order=(p, d, q))
resultado_arima = modelo_arima.fit()

# ğŸ“Œ 8. PredicciÃ³n con ARIMA
horizonte = 10  # NÃºmero de periodos futuros
prediccion = resultado_arima.forecast(steps=horizonte)

# ğŸ“Œ Crear Fechas Futuras para la PredicciÃ³n
ult_fecha = df.index[-1]
fechas_futuras = [ult_fecha + pd.DateOffset(months=i) for i in range(1, horizonte+1)]

# ğŸ“Œ 9. Visualizar la PredicciÃ³n ARIMA
plt.figure(figsize=(10, 5))
plt.plot(df.index, df['Valor'], label="Serie Original", color="blue")
