plt.title("ğŸ“ˆ PredicciÃ³n ARIMA de la Serie Temporal")
plt.xlabel("Fecha")
plt.ylabel("Valor")
plt.legend()
plt.grid()
plt.xticks(rotation=45)
plt.show()

# ğŸ“Œ 10. Mostrar el Resumen del Modelo
print("\nğŸ“ˆ Resumen del modelo ARIMA:")
print(resultado_arima.summary())

